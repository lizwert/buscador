% Funcion que busca y rescata todas las IDs de una lista de ID y
% frecuencias asociada a un termino.
%
% ENTRADA: un termino en forma de String -> "termino".
%
% SALIDA: una lista con todas las IDs de los documentos donde este
% termino existe -> [X1, X2, X3, X4...]
singleTermQuery(Term, Result):-
    not(stopWords(Term)),
    indice(Term,LResult),
    getAllIDs(LResult,Result).






% Funcion que busca y rescata todas las IDs de una lista de ID y
% frecuencias asociada a un termino, agregandolas a una nueva lista con
% todas las IDs, En esta lista pueden repetirse las IDs agragadas.
%
% ENTRADA: una lista de terminos -> ["termino1", "termino2", .....]
%
% SALIDA: una lista con todas las IDs de los documentos donde estos
% terminos existen -> [X1, X2, X3, X4, X1, X4, X6, ......]


